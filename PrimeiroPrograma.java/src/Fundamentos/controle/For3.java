package Fundamentos.controle;

public class For3 {
    public static void main(String[] args) {

        int i = 0;
        for (; i < 10; i++) {
            System.out.println(i);
        }

        System.out.println("\nSaiu do FOR...");
        System.out.println(i);
    }
}

